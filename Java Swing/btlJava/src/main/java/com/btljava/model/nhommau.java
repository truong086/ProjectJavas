package com.btljava.model;

/**
 *
 * @author ASUS
 */
public class nhommau {
    int id;
    String ten;
    String ngaytao;

    public nhommau() {
    }

    public nhommau(int id, String ten, String ngaytao) {
        this.id = id;
        this.ten = ten;
        this.ngaytao = ngaytao;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getNgaytao() {
        return ngaytao;
    }

    public void setNgaytao(String ngaytao) {
        this.ngaytao = ngaytao;
    }
    
    
}

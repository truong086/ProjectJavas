    private void phanhoisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_phanhoisMouseClicked
        // TODO add your handling code here:
        try{
            int row = phanhois.getSelectedRow();
            if(row >= 0){
                ids = (int) phanhois.getValueAt(row, 0);
//                phanhoiDao dao = new phanhoiDao();
                phanhoi ph = dao.findbyid(ids);
                String nguoigui = String.valueOf(ph.getNguoigui_id());
                String nguoinhan = String.valueOf(ph.getNguoinhan_id());
                if(ph != null){
                    nguoiguiph.setText(nguoigui);
                    nguoinhans.setText(nguoinhan);
                    tieudeph.setText(ph.getTieude());
                    chucvus.setText(ph.getChucvu());
                    noidungph.setText(ph.getNoidung());
                    ngayguiph.setText(ph.getNgaygui());
                }else{
                    MessageDialog.showMessageError(this, "Loi", "Loi");
                    return;
                }
            }
        }catch(Exception e){
            e.printStackTrace();
            MessageDialog.showMessageError(this, e.getMessage(), "Lỗi");
        }
        
        
    }//GEN-LAST:event_phanhoisMouseClicked


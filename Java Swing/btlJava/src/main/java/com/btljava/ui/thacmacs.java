    private void loadDataTable(){
        int phanquyens = 0;
        String phanquyen = String.valueOf(ShereData.dangnhap.getTentaikhoan());
        phanquyens = Integer.parseInt(phanquyen);
        try{
//            phanhoiDao dao = new phanhoiDao();
            List<phanhoi> list = dao.findAll(phanquyens);
            model.setRowCount(0);
            for(phanhoi ph : list){
                model.addRow(new Object[] {
                    ph.getId(), ph.getNguoigui_id(), ph.getChucvu(), ph.getNoidung()
                });
            }
            
            model.fireTableDataChanged();
        }catch(Exception e){
            e.printStackTrace();
            MessageDialog.showMessageError(this, e.getMessage(), "Lỗi");
        }
    }
    

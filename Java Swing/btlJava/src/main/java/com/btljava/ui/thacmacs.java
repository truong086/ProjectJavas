    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int nguoigui = 0;
        
        StringBuilder sb = new StringBuilder();
        
        DataValidor.kiemtra(nguoiguiid, sb, "Người gửi không được để trống");
        DataValidor.kiemtra(tieudes, sb, "Tiêu đề không được để trống");
        DataValidor.kiemtra(sodienthoais, sb, "Số điện thoại không được để trống");
        DataValidor.kiemtra(emails, sb, "Email không được để trống");
        DataValidor.kiemtra(diachis, sb, "Địa chỉ không được để trống");
        DataValidor.kiemtra(ngayguis, sb, "Ngày gửi không được để trống");
        DataValidor.kiemtraArea(noidungs, sb, "Nội dung không được để trống");
        
        if(sb.length() > 0){
            MessageDialog.showMessageError(this, sb.toString(), "Lỗi");
            return;
        }
        
        String email = emails.getText();
        String check = "^[a-zA-Z0-9_!#$%&'*+/=?`{|}~^.-]+@[a-zA-Z0-9.-]+$";
        
        
        try{
            nguoigui = Integer.parseInt(nguoiguiid.getText());
            thacmac tm = new thacmac();
            tm.setNguoigui_id(nguoigui);
            tm.setTieude(tieudes.getText());
            tm.setSodienthoai(sodienthoais.getText());
            if(patternMatches(email, check)){ // Kiểm tra nếu người dùng nhập đúng định dạng email thì bắt đầu gán dữ liệu cho biến ánh xạ email trong database
                tm.setEmail(email); // Gán dữ liệu cho biến ánh xạ email trong database
            }else{ // Nếu không đúng định dạng email thì in ra thông báo lỗi
                MessageDialog.showMessageError(this, "Không đúng định dạng email", "Lỗi"); // In ra thông báo lỗi
                return;
            }
            
            tm.setDiachi(diachis.getText());
            tm.setNoidung(noidungs.getText());
            tm.setNgaygui(ngayguis.getText());
//            thacmacDao dao = new thacmacDao();
            if(daos.insert(tm)){
                MessageDialog.showMessageDialog(this, "Gửi thành công", "Thông báo");
                loadDataTable();
            }else{
                MessageDialog.showMessageError(this, "Đã có lỗi xảy ra", "Lỗi");
            }
        }catch(Exception e){
            e.printStackTrace();
            MessageDialog.showMessageError(this, e.getMessage(), "Lỗi");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed


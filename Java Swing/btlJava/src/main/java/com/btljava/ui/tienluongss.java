    private void tbltableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbltableMouseClicked
        // TODO add your handling code here:

        try{
            int row = tbltable.getSelectedRow();
            if(row >= 0){
                ids = (int) tbltable.getValueAt(row, 0);
//                tienluongDao dao = new tienluongDao();
                tienluong tl = dao.findbyid(ids);
                String matienluong = String.valueOf(tl.getMatienluong());
                String tienluong = String.valueOf(tl.getTienluong());
                String ngaycong = String.valueOf(tl.getNgaycong());

                if(tl != null){
                    matienluongs.setText(matienluong);
                    tienluongs.setText(tienluong);
                    ngaycongs.setText(ngaycong);
                    ngaytaos.setText(tl.getNgaytao());
                }
            }
        }catch(Exception e){
            e.printStackTrace();
            MessageDialog.showMessageError(mains, e.getMessage(), "lỗi");
        }
    }//GEN-LAST:event_tbltableMouseClicked


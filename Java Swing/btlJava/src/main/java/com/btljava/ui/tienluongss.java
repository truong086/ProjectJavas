    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int matienluong = 0;
        int tienluong = 0;
        int ngaycong = 0;

        StringBuilder sb = new StringBuilder();
        DataValidor.kiemtra(matienluongs, sb, "Mã tiền lương không được để trống");
        DataValidor.kiemtra(tienluongs, sb, "Tiền lương không được để trống");
        DataValidor.kiemtra(ngaycongs, sb, "Ngày công không được để trống");
        DataValidor.kiemtra(ngaytaos, sb, "Ngày tạo không được để trống");

        if(sb.length() > 0){
            MessageDialog.showMessageError(mains, sb.toString(), "lỗi");
            return;
        }

        if(matienluongs.getText().chars().allMatch(Character :: isDigit)
            && tienluongs.getText().chars().allMatch(Character :: isDigit)
            && ngaycongs.getText().chars().allMatch(Character :: isDigit)){
            matienluong = Integer.parseInt(matienluongs.getText());
            tienluong = Integer.parseInt(tienluongs.getText());
            ngaycong = Integer.parseInt(ngaycongs.getText());
        }else{
            MessageDialog.showMessageError(mains, "Mã tiền lương và ngày công và tiền lương phải là số", TOOL_TIP_TEXT_KEY);
        }
        try{
            tienluong tl = new tienluong();
            tl.setMatienluong(matienluong);
            tl.setTienluong(tienluong);
            tl.setNgaycong(ngaycong);
            tl.setNgaytao(ngaytaos.getText());
//            tienluongDao dao = new tienluongDao();
            if(dao.findby(matienluongs.getText()) != null){
                tienluong tls = new tienluong();
                tls.setId(ids);
                tls.setMatienluong(matienluong);
                tls.setTienluong(tienluong);
                tls.setNgaycong(ngaycong);
                tls.setNgaytao(ngaytaos.getText());
                if(MessageDialog.showConfirmDialog(mains, "Bạn muốn cập nhật dữ liệu không", "Thông báo") == JOptionPane.NO_OPTION){
                    return;
                }else if(dao.update(tls)){
                    MessageDialog.showMessageDialog(mains, "Update thành công", "Thông báo");
                    loadDatatable();
                }else{
                    MessageDialog.showMessageError(mains, "Đã có lỗi xảy ra", "lỗi");
                }

            }else{
                if(dao.insert(tl)){
                    MessageDialog.showMessageDialog(mains, "Thêm dữ liệu thành công", "Thông báo");
                    loadDatatable();
                }else{
                    MessageDialog.showMessageError(mains, "Đã có lỗi xảy ra", "Lỗi");
                }
            }

        }catch(Exception e){
            e.printStackTrace();
            MessageDialog.showMessageError(mains, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


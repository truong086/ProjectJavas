    private void loadDatatable(){
        try{
//            tienluongDao dao = new tienluongDao();
            List<tienluong> list = dao.findAll();
            model.setRowCount(0);
            for(tienluong tl : list){
                model.addRow(new Object[] {
                    tl.getId(), tl.getMatienluong(), tl.getTienluong(), tl.getNgaycong(), tl.getNgaytao()
                });
            }
            
            model.fireTableDataChanged();
        }catch(Exception e){
            e.printStackTrace();
            MessageDialog.showMessageError(mains, e.getMessage(), "lỗi");
        }
    }
    

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int nguoigui = 0;
        int nguoinhan = 0;
        
        StringBuilder sb = new StringBuilder();
        
        DataValidor.kiemtra(nguoiguiph, sb, "Người gửi không được để trống");
        DataValidor.kiemtra(nguoinhans, sb, "Người nhận không được để trống");
        DataValidor.kiemtra(tieudeph, sb, "Tiêu đề không được để trống");
        DataValidor.kiemtra(chucvus, sb, "Chức vụ không được để trống");
        DataValidor.kiemtraArea(noidungph, sb, "Nội dung không được để trống");
        DataValidor.kiemtra(ngayguiph, sb, "Ngày gửi không được để trống");
        
        if(sb.length() > 0){
            MessageDialog.showMessageError(this, sb.toString(), "Lỗi");
            return;
        }
        
        
        try{
            nguoigui = Integer.parseInt(nguoiguiph.getText());
            nguoinhan = Integer.parseInt(nguoinhans.getText());
            phanhoi ph = new phanhoi();
            ph.setNguoigui_id(nguoigui);
            ph.setNguoinhan_id(nguoinhan);
            ph.setTieude(tieudeph.getText());
            ph.setChucvu(chucvus.getText());
            ph.setNoidung(noidungph.getText());
            ph.setNgaygui(ngayguiph.getText());
//            phanhoiDao dao = new phanhoiDao();
            if(daos.insert(ph)){
                MessageDialog.showMessageDialog(this, "Gửi thành công", "Lỗi");
                loadDatatable();
            }else{
                MessageDialog.showMessageError(this, "Đã có lỗi xảy ra", "Lỗi");
            }
            
        }catch(Exception e){
            e.printStackTrace();
            MessageDialog.showMessageError(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


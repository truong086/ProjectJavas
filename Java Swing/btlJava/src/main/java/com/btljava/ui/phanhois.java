    private void thacmacsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_thacmacsMouseClicked
        // TODO add your handling code here:
        try{
            int row = thacmacs.getSelectedRow();
            if(row >= 0){
                ids = (int) thacmacs.getValueAt(row, 0);
//                thacmacDao dao = new thacmacDao();
                thacmac tm = dao.findbyid(ids);
                String nguoigui = String.valueOf(tm.getNguoigui_id());
                
                if(tm != null){
                    nguoiguiid.setText(nguoigui);
                    tieudes.setText(tm.getTieude());
                    sodienthoais.setText(tm.getSodienthoai());
                    emails.setText(tm.getEmail());
                    diachis.setText(tm.getDiachi());
                    ngayguis.setText(tm.getNgaygui());
                    noidungs.setText(tm.getNoidung());
                    trangthais.setText(tm.getTrangthai());
                }
            }
        }catch(Exception e){
            e.printStackTrace();
            MessageDialog.showMessageError(this, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_thacmacsMouseClicked


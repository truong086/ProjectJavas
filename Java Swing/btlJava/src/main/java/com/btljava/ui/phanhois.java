    private void loadDatatable(){
        try{
//            thacmacDao dao = new thacmacDao();
            List<thacmac> list = dao.findAll();
            List<thacmac> lists = dao.deleteThacMac();
            
            for(thacmac tm : lists){
                tm.getId();
                
                String tms = String.valueOf(tm.getId());
                dao.delete(tms);  
            }
            model.setRowCount(0);
            for(thacmac tm : list){
                model.addRow(new Object[] {
                    tm.getId(), tm.getNguoigui_id(), tm.getTieude(), tm.getSodienthoai(), tm.getEmail(), tm.getDiachi(), tm.getNoidung(), tm.getTrangthai(), tm.getNgaygui()
                });
            }
            
            model.fireTableDataChanged();
        }catch(Exception e){
            e.printStackTrace();
            MessageDialog.showMessageError(this, e.getMessage(), "Lỗi");
        }
    }
    
